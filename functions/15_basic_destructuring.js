const array = [1, 2, 3]

// Vai pegar o primeiro elemento do array e o segundo elemento
// Vai atribuir ao x e y, ficando dessa forma [a = 1, b = 2]
const [a, b] = array

// Destructuring - Leia os atributos de dentro de uma estrutura
// Vai atribuir primeiro valor a x, vai ignorar o segundo, vai atribuir terceiro valor a y
// Ficando dessa forma [x = 1, y = 3]
const [x,, y] = array